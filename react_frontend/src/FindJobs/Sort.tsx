import { useState } from 'react';
import { Button, Combobox, useCombobox, Text, Box } from '@mantine/core';
import { IconAdjustments } from '@tabler/icons-react';

const opt = ['Liên quan', 'Gần đây nhất', 'Lương (Thấp đến Cao)', 'Lương (Cao đến Thấp)'];

const Sort=()=> {
  const [selectedItem, setSelectedItem] = useState<string | null>('Liên quan');
  const combobox = useCombobox({
    onDropdownClose: () => combobox.resetSelectedOption(),
  });

  const options = opt.map((item) => (
    <Combobox.Option value={item} key={item}>
      {item}
    </Combobox.Option>
  ));

  return (
    <>
      

      <Combobox
        store={combobox}
        width={250}
        position="bottom-start"
        withArrow
        onOptionSubmit={(val) => {
          setSelectedItem(val);
          combobox.closeDropdown();
        }}
      >
        <Combobox.Target>
        
          <div onClick={()=>combobox.toggleDropdown()} className=' cursor-pointer border border-bright-sun-400 flex px-2 py-1 rounded-xl'>
            {selectedItem} <IconAdjustments/>
          </div>
        </Combobox.Target>

        <Combobox.Dropdown>
          <Combobox.Options>{options}</Combobox.Options>
        </Combobox.Dropdown>
      </Combobox>
    </>
  );
}
export default Sort;